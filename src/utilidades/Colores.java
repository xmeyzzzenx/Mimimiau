package utilidades;

/**
 * Clase que guarda códigos de colores para escribir en la consola.
 * Permite cambiar el color del texto que se muestra.
 * Incluye colores normales y colores en negrita (bold).
 * 
 * @author Ximena Meyzen Calderón
 * @version 1.8 (final)
 */
public class Colores {

    /** Código para volver al color normal. */
    public static final String RESET = "\u001B[0m";

    /** Código para color negro. */
    public static final String NEGRO = "\u001B[30m";
    /** Código para color rojo. */
    public static final String ROJO = "\u001B[31m";
    /** Código para color verde. */
    public static final String VERDE = "\u001B[32m";
    /** Código para color amarillo. */
    public static final String AMARILLO = "\u001B[33m";
    /** Código para color azul. */
    public static final String AZUL = "\u001B[34m";
    /** Código para color morado. */
    public static final String MORADO = "\u001B[35m";
    /** Código para color cyan (celeste). */
    public static final String CYAN = "\u001B[36m";
    /** Código para color blanco. */
    public static final String BLANCO = "\u001B[37m";

    /** Código para color negro en negrita (bold). */
    public static final String NEGRO_BOLD = "\u001B[1;30m";
    /** Código para color rojo en negrita (bold). */
    public static final String ROJO_BOLD = "\u001B[1;31m";
    /** Código para color verde en negrita (bold). */
    public static final String VERDE_BOLD = "\u001B[1;32m";
    /** Código para color amarillo en negrita (bold). */
    public static final String AMARILLO_BOLD = "\u001B[1;33m";
    /** Código para color azul en negrita (bold). */
    public static final String AZUL_BOLD = "\u001B[1;34m";
    /** Código para color morado en negrita (bold). */
    public static final String MORADO_BOLD = "\u001B[1;35m";
    /** Código para color cyan en negrita (bold). */
    public static final String CYAN_BOLD = "\u001B[1;36m";
    /** Código para color blanco en negrita (bold). */
    public static final String BLANCO_BOLD = "\u001B[1;37m";
}
